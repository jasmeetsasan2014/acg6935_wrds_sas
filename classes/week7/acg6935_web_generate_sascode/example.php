<h3>PHP - short intro</h3>

<p>Normally, PHP (PHP Hypertext Processor) is used to add some 'logic' to HTML.</p>

<p>You can embed PHP code into the HTML page, like so: <?php echo "This is PHP printing text" ;?></p>

<p>More useful is to use programming features; for example, this is the date/time: <?php echo date("l jS \of F Y h:i:s A"); ?> </p>

<p>When you 'view page source' in the browser, you won't see any PHP; PHP is executed on the server, and the result is shipped to the browser.</p>

<p>You are however not limited to generate HTML; you can also generate SAS code (or C++ source code, etc).</p>
